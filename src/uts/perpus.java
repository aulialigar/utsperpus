    private void dataMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_dataMouseClicked
        // TODO add your handling code here:
        int baris = data.getSelectedRow();
        if (baris != -1) {
            no.setText(data.getValueAt(baris, 0).toString());
            nama.setText(data.getValueAt(baris, 1).toString());
            alamat.setText(data.getValueAt(baris, 2).toString());
            if ("Pelajaran".equals(data.getValueAt(baris, 3).toString())) {
                jenis.setSelectedItem(true);
            } else if(jenis.getSelectedItem().equals("Novel")){
                jenis.setSelectedItem(true);
            } else if(jenis.getSelectedItem().equals("Komik")){
                jenis.setSelectedItem(true);
            }
            judul.setText(data.getValueAt(baris, 4).toString());
            ((JTextField)pinjam.getDateEditor().getUiComponent()).setText(data.getValueAt(baris, 5).toString());
            ((JTextField)kembali.getDateEditor().getUiComponent()).setText(data.getValueAt(baris, 6).toString());
            denda.setText(data.getValueAt(baris, 7).toString());
            if ("Kembali".equals(data.getValueAt(baris, 8).toString())) {
                sudah.setSelected(true);
            } else {
                belum.setSelected(true);
            }
        }
    }//GEN-LAST:event_dataMouseClicked

