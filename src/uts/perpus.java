    private void saveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveActionPerformed
        // TODO add your handling code here:
        SimpleDateFormat dateFormat = new SimpleDateFormat("yyyy-MM-yy");
        String tanggalp = dateFormat.format(pinjam.getDate());
        String tanggalk = dateFormat.format(kembali.getDate());
        String ST = "";
        if (sudah.isSelected()) {
                    ST = "Kembali";
                } else {
                    ST = "Belum Kembali";
                }
        String JN = "";
        if (jenis.getSelectedItem().equals("Pelajaran")) {
                    JN = "Pelajaran";
                } else if(jenis.getSelectedItem().equals("Novel")){
                    JN = "Novel";
                } else if(jenis.getSelectedItem().equals("Komik")){
                    JN = "Komik";
                }
        if ("".equals(no.getText()) || "".equals(nama.getText()) || "".equals(alamat.getText()) || "".equals(JN)
                || "".equals(judul.getText()) || "".equals(tanggalp) || "".equals(tanggalk) || "".equals(denda.getText()) || "".equals(ST)){
            JOptionPane.showMessageDialog(this, "Harap Lengkapi Data", "Error", JOptionPane.WARNING_MESSAGE);
        } else {
            String SQL = "INSERT INTO t_siswa (NoAnggota,NamaPeminjam,AlamatPeminjam,JenisBuku,JudulBuku,TanggalPinjam,TanggalKembali,Denda,Status)"
            + "VALUES('"+no.getText()+"','"+nama.getText()+"','"+ alamat.getText()+"','"+JN+"','"+judul.getText()+"','"+tanggalp
            +"','"+tanggalk+"','"+denda.getText()+"','"+ST+"')";
            int status = KoneksiDB.execute(SQL);
            if (status == 1) {
                JOptionPane.showMessageDialog(this, "Data berhasil ditambah", "Sukses", JOptionPane.INFORMATION_MESSAGE);
                selectData();
            } else {
                JOptionPane.showMessageDialog(this, "Data gagal ditambahkan", "Sukses", JOptionPane.WARNING_MESSAGE);
            }
        }
    }//GEN-LAST:event_saveActionPerformed

    private void delActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_delActionPerformed
        // TODO add your handling code here:
        int baris = data.getSelectedRow();
        if (baris != -1) {
            String NoAnggota = data.getValueAt(baris, 0).toString();
            String SQL = "DELETE FROM t_siswa WHERE NoAnggota='"+NoAnggota+"'";
            int status = KoneksiDB.execute(SQL);
            if (status == 1) {
                JOptionPane.showMessageDialog(this, "Data berhasil dihapus", "Sukses", JOptionPane.INFORMATION_MESSAGE);
            } else {
                JOptionPane.showMessageDialog(this, "Data gagal dihapus", "Gagal", JOptionPane.WARNING_MESSAGE);
            }
        } else {
            JOptionPane.showMessageDialog(this, "Pilih Baris Data Terlebih Dahulu", "Error", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_delActionPerformed

